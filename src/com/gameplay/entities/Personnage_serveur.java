package com.gameplay.entities;

public class Personnage_serveur 
{
	private String nom;
	private int posx;
	private int posy;
	
	public Personnage_serveur(String nom)
	{
		this.nom = nom;
	}

	public String getNom() {
		return nom;
	}

	public void setNom(String nom) {
		this.nom = nom;
	}

	public int getPosx() {
		return posx;
	}

	public void setPosx(int posx) {
		this.posx = posx;
	}

	public int getPosy() {
		return posy;
	}

	public void setPosy(int posy) {
		this.posy = posy;
	}
}
